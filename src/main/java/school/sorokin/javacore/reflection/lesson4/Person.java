package school.sorokin.javacore.reflection.lesson4;

public class Person {

    @NotNull
    private String name;

    @Size(min = 1, max = 120)
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
